package ec.unomas.notadebito;

public class Motivos {
	private String razon;
	private Double valor;
	
	public void setRazon(String razon){
		this.razon = razon;
	}
	public String getRazon(){
		return this.razon;
	}
	
	public void setValor(Double valor){
		this.valor = valor;
	}
	public Double getValor(){
		return this.valor;
	}

}
