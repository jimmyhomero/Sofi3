private void btnDescargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDescargarActionPerformed
// TODO add your handling code here:
    //Obtenemos el posible nombre del archivo a descargar
    String nombreRecurso = txtURL.getText();
    String posibleNombreArchivo = "";
    if (nombreRecurso != null && !nombreRecurso.isEmpty()) {
        String[] tokens = nombreRecurso.split("/");
        int cantidadTokens = tokens.length;
        if (tokens != null && cantidadTokens > 0) {
            posibleNombreArchivo = tokens[cantidadTokens - 1];
        }
    }
    //Mostramos el diálogo para indicar el lugar donde se descargará el archivo
    File archivoDescarga = new File(posibleNombreArchivo);
    selectorArchivoDescarga.setSelectedFile(archivoDescarga);
    int opcion = selectorArchivoDescarga.showSaveDialog(this);
    if (opcion == JFileChooser.APPROVE_OPTION) {
        String ruta = selectorArchivoDescarga.getSelectedFile().getPath();
        Gestor gestor = new Gestor(nombreRecurso, ruta, this);
        Thread thread = new Thread(gestor);
        thread.start();
    }

}//GEN-LAST:event_btnDescargarActionPerformed

