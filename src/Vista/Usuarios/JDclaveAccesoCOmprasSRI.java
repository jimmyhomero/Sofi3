    private void consultar() {
       

            String tipo = jTextField1.getText().substring(8, 10);
            if (jTextField1.getText().trim().length() == 49 && ValidaNUmeros.isOnlyNumbers(jTextField1.getText().trim())) {
                if ((tipo.equals("01") || tipo.equals("03"))) {

                    ComprasDao cDao = new ComprasDao();
                    Compras cx = new Compras();

                    cx = cDao.buscarbyCLaveAcceso(jTextField1.getText().trim());
                    if (cx != null) {
                        // if (cx.getCalveAcceso()==txt_numAutorizacion.getText().trim()) {
                        ProgressBar.mostrarMensajeAzul("Clave de Acceso ya esta registrada");
                        jTextField1.setBackground(java.awt.Color.yellow);

                        // jButton1.setEnabled(false);
                    } else {
                        ProgressBar.mostrarMensajeVerde("BUSCANDO EN LINEA... ESPERE...");
                        Leertxt.descargarXMLformSRItoFileXMLandPFDenUnSoloPasoclickjtable(jTextField1.getText().trim());
                        String file = OperacionesForms.rutadocPDFgeneradook;//Config.AUTORIZADOS_DIR +claveAcceso + ".pdf";
                        //  JOptionPane.showMessageDialog(null, file);
// if (Comprobante.VerificasiExisteConecionconWSdelSRI(claveAcceso)) {                
                        Modal_fac_electronica_relacionarProductos mcc = new Modal_fac_electronica_relacionarProductos();
                        mcc.txtNumeroAutorizacion.setText(jTextField1.getText().trim());
                        OperacionesForms.solocrearFacturaNOgenerrarPDF = false;
                        if (mcc.llenarCompraviaclaveAccesoVerificaExistenciadexxxxxxxx()) {
                            OperacionesForms.nuevaVentanaInternalForm(mcc, mcc.getTitle(), true);
                            this.dispose();
                        } else {
                            mcc.dispose();
                        }

//                    }else{
//                    ProgressBar.mostrarMensajeAzul("SRI SIN CONECCION");
//                    }
                    }

                } else {
                    ProgressBar.mostrarMensajeAzul("DOCUMENTO ELECTRONICA NO PERMITIDO");
                }
            } else {
                ProgressBar.mostrarMensajeAzul("CLAVE DE ACCESO MAL FORMADA");
            }
        
    }

