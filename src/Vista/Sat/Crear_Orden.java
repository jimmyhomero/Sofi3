    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:        
        if (!txtSerie.getText().equals("") || jcbAparatos.getItemCount() > 0) {
            SatOrdenDetalle satOrdenDetalle = new SatOrdenDetalle();
            SatOrdenDetalleDao satOrdenDetalleDao = new SatOrdenDetalleDao();
            SatOrden satOrden = new SatOrden();
            SatOrdenDao satOrdenDao = new SatOrdenDao();
            satOrden.setClientes_codigo(newCliente.getCodigo());
            satOrden.setDescripcion(txtObservaciones.getText());
            satOrden.setNumero(txt_numeroOrden.getText());
            satOrden.setUsuarios_codigo(login.login.CodigoUsuario);
            //OrdenDetalle
            satOrdenDetalle.setCerrado(0);
            satOrdenDetalle.setAccesorios(txtAccesorios.getText());
            satOrdenDetalle.setPresupuesto(Double.parseDouble(txtPresupuesto.getText().replace(",", ".")));
            satOrdenDetalle.setEstado("1");
            OrdenCodigo = satOrdenDao.guardar(satOrden);
            satOrdenDetalle.setSatorden_codigo(OrdenCodigo);
            satOrdenDetalle.setSatAparato_codigo(aparatoCodigo);
            satOrdenDetalleDao.guardar(satOrdenDetalle);
            llenarNextNumeroOrdenLBL();
            vaciartxt();

        } else {
            JOptionPane.showMessageDialog(null, "SELECCIONA UN APARATO REGISTRADO ", "ERROR", JOptionPane.ERROR_MESSAGE, frameIcon);
        }

        ////guardar

    }//GEN-LAST:event_jButton4ActionPerformed
