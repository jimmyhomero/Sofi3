    public static String getGestorBDD() {
        String ruta = null;
        switch (combo.getSelectedItem().toString()) {
            case "MYSQL":
                ruta = Config.RAIZ + "MYSQL" + Config.SEPARADOR + "bin" + Config.SEPARADOR + "mysqldump";
                break;
            case "XAMPP":
                ruta = Config.RAIZ + "xampp" + Config.SEPARADOR + "mysql" + Config.SEPARADOR + "bin" + Config.SEPARADOR + "mysqldump";

                break;
            case "WAMPP":
                break;

        }
        return ruta;
    }

    public String selectDirectorio(String gestor) {
        String ruta = null;

        int resp;

        Date now = new Date();
        JFileChooser explorador = new JFileChooser(Config.RAIZ);
        //resp = explorador.showSaveDialog(this);

        int seleccion = explorador.showSaveDialog(null);
//analizamos la respuesta
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Sql", ".sql");
        explorador.setFileFilter(filter);
        explorador.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
    //    File backupFile = new File(String.valueOf(explorador.getCurrentDirectory().getAbsoluteFile()) + Config.SEPARADOR+"Respaldo.sql");              
///        Deb.consola("Vista.Backup.Crear_Backup.selectDirectorio()sssssssssss: "+explorador.getSelectedFile().getName());
        
                
        switch (seleccion) {

            case JFileChooser.APPROVE_OPTION:
                ruta = String.valueOf(explorador.getCurrentDirectory().getAbsoluteFile()) + Config.SEPARADOR+"Respaldo.sql";//+now.getDay() +"-"+now.getMonth()+"-"+now.getYear()+".sql";
              //  
                BackupMysql.respaldar(ruta, gestor);
                
                //   Deb.consola("Ruta:  " + ruta);
                //seleccionó abrir
                break;

            case JFileChooser.CANCEL_OPTION:
                //dio click en cancelar o cerro la ventana
                ruta = "";
                break;

            case JFileChooser.ERROR_OPTION:
                //llega aqui si sucede un error
                ruta = "";
                JOptionPane.showMessageDialog(null, "Error al Seleccionar Direcctorio");
                break;
        }

        return ruta;
    }

